            <h2 id="rekisteriseloste">Rekisteriseloste</h2>

            <p>Joulukorttipalvelua ylläpitävä Ekokumppanit Oy tallentaa lähettäjien ja vastaanottajien nimen sekä sähköpostiosoitteen kampanjan keston ajaksi. Näitä tietoja käytetään ainoastaan lähetettyjen korttien rekisteröintiin ja palkintojen voittajien arvontaan vastaanottajien joukosta.</p>

            <p>Tietoja ei luovuteta eteenpäin eikä käytetä muuhun kuin korttipalveluun liittyvään viestintään. Palkintojen voittajien nimet julkaistaan hankkeen verkkosivuilla.</p>

