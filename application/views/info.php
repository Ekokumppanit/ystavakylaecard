            <h2 id="info">Joulukorttikampanja</h2>

            <p>Ystäväkylä-hankkeen toteuttama Joulukorttikampanja on ensisijaisesti Pirkanmaan kylille suunnattu ja räätälöity väline sähköisten joulutervehdysten lähettämiseen. Korttien pohjakuvia haettiin <a target="_blank" href="http://www.pirkankylat.fi/">Pirkan kylät ry:n</a> avulla maakunnan kyläyhdistyksiltä ja arvonnan palkinnot lahjoittivat <a target="_blank" href="https://www.s-kanava.fi/web/s/pirkanmaa">Pirkanmaan Osuuskauppa</a> ja <a target="_blank" href="http://www.lahitapiola.fi/">LähiTapiola</a>. Korttien lähetyspalvelu on ilmainen ja avoin kaikille käyttäjille. Loukkaavien ja hyvän maun vastaisten viestien lähettäminen on kielletty.</p>


